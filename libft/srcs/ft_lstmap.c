#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*tmp;
	void	*container;

	new = NULL;
	while (lst)
	{
		container = f(lst->container);
		tmp = ft_lstnew(container);
		if (!tmp)
		{
			del(container);
			ft_lstclear(&new, del);
			return (NULL);
		}
		ft_lstadd_back(&new, tmp);
		lst = lst->next;
	}
	return (new);
}
