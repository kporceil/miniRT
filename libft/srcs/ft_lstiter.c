#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	tmp = lst;
	while (tmp)
	{
		lst = lst->next;
		f(tmp->container);
		tmp = lst;
	}
}
