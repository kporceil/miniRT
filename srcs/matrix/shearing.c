#include "matrix.h"

t_matrix	matrix_shearing(double x[2], double y[2], double z[2])
{
	return ((t_matrix){
		{{1, x[0], x[1], 0},
			{y[0], 1, y[1], 0},
			{z[0], z[1], 1, 0},
			{0, 0, 0, 1}}, 4});
}
