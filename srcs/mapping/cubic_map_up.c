#include "patterns.h"

void	cubic_map_up(t_tuple p, double *u, double *v)
{
	*u = (p.x + 1.0) / 2.0;
	*v = (1.0 - p.z) / 2.0;
	*u = fmin(fmax(*u, 0.0), 1.0);
	*v = fmin(fmax(*v, 0.0), 1.0);
}
